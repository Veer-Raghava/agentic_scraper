"""
agents/extractor_agent.py — Structured data extraction agent.

Responsibilities:
  1. For each ScrapedDocument, build a precision-crafted extraction prompt.
  2. Send text to the LLM and parse the JSON response.
  3. Score per-field confidence so the Critic knows what to verify.
  4. Handle chunked extraction (large documents) and merge results.
  5. Write ExtractedRow objects into state.rows.

Prompt engineering philosophy (this is the most critical part of the system):
  • System prompt tells the LLM its exact role and output format.
  • Field definitions are generated by the LLM itself (not hardcoded) based on
    the column names AND the topic — this ensures definitions are domain-appropriate.
  • A realistic example row is generated by the LLM and injected as a few-shot
    hint — this dramatically reduces hallucinations.
  • Explicit rules prevent common failure modes: don't invent data, don't merge
    rows, output N/A not null/empty, one object per distinct item.
  • For multi-entity documents (lists of drugs, papers, companies) the LLM
    returns multiple JSON objects.
  • For single-entity documents (one paper, one company page) it returns one.
"""

from __future__ import annotations

import json
import re
import time
from typing import Any

from rich.console import Console

import config as cfg
from agents.scraper_agent import ScrapedDocument
from state import ColumnDef, ExtractedRow, SessionState
from tools.llm_tools import LLMClient
from tools.pdf_tools import chunk_text, select_best_chunks

console = Console()

# Phrases the LLM uses for missing values — these all map to N/A
_EMPTY_VALS = frozenset({
    "n/a", "none", "not specified", "not mentioned", "not provided",
    "not explicitly mentioned", "unspecified", "unknown", "na", "nil", "",
    "not available", "not found", "not discussed", "not applicable",
    "not stated", "not reported", "not described", "null", "—", "-",
})

# Max chunks to send in a single Ollama-style chunked extraction
_MAX_CHUNKS_PER_CALL = 6


def _is_empty(v: str) -> bool:
    return str(v).strip().lower() in _EMPTY_VALS


# ═══════════════════════════════════════════════════════════════════════════════
# Prompt builders
# ═══════════════════════════════════════════════════════════════════════════════

def _build_system_prompt(
    columns: list[ColumnDef],
    topic: str,
    example_json: str,
    is_table: bool = False,
) -> str:
    """
    Build the system prompt for extraction.

    This is the most carefully crafted prompt in the system.
    It tells the LLM exactly what to do, what not to do, and gives
    a concrete example of the expected output format.

    Args:
        columns      — the ColumnDef objects with name, description, example
        topic        — the research topic (for context)
        example_json — a JSON string showing one realistic example row
        is_table     — True when the input is already a structured table

    Returns:
        A complete system prompt string.
    """
    # Build the field block — this is the heart of the prompt
    field_lines = []
    for col in columns:
        line = f'  "{col.name}": {col.description}'
        if col.example:
            line += f' Example: {col.example}.'
        field_lines.append(line)
    field_block = "\n".join(field_lines)

    entity_instruction = (
        "This is a STRUCTURED TABLE — return exactly ONE JSON object per data row "
        "(excluding header rows)."
        if is_table
        else (
            "Identify every DISTINCT ENTITY relevant to the topic in this text. "
            "If multiple entities exist (multiple drugs, papers, companies, etc.), "
            "return ONE JSON object per entity. "
            "If the text focuses on a single entity, return exactly one object."
        )
    )

    return f"""\
You are an expert data extraction agent specialising in {topic}.

YOUR TASK:
Extract structured information from the provided text and return it as a JSON array.

FIELD DEFINITIONS (extract EXACTLY these fields, using these precise definitions):
{field_block}

OUTPUT FORMAT:
- Return a JSON ARRAY of objects, one per distinct entity found.
- Every object must have ALL of the following keys: {', '.join(f'"{c.name}"' for c in columns)}
- Use the string "N/A" (not null, not empty, not 0) for any field where the information
  is genuinely absent from the text. Do NOT guess or invent values.
- Use exact values as written in the source text — do not paraphrase or summarize field values.
- For numeric fields, include units when present (e.g. "342 mg/kg", not just "342").
- For date fields, use the format found in the text (e.g. "March 2023", "2023-03-15").

CRITICAL RULES:
1. NEVER invent or hallucinate data. If you are not certain, use "N/A".
2. NEVER merge two distinct entities into one object.
3. NEVER omit a field — every object must have every key, even if the value is "N/A".
4. NEVER return prose, explanation, or markdown. ONLY return the JSON array.
5. {entity_instruction}
6. For confidence: if a value is explicitly stated in the text, you are confident.
   If you inferred or approximated it, use "N/A" — the Refiner will find it.

EXAMPLE OUTPUT (for reference — use actual values from the text, not these examples):
{example_json}

Now extract from the provided text. Start your response with [ immediately."""


def _build_user_message(doc: ScrapedDocument) -> str:
    """
    Build the user message containing the text to extract from.
    Prepends source context to help the LLM understand provenance.
    """
    source_line = (
        f"[SOURCE: {doc.source_url[:120]}]\n\n"
        if doc.source_url else ""
    )
    return source_line + doc.full_context()


